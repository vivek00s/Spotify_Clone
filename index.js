let allContent = document.getElementById('content');
let isClicked = false; //hide initially

function hideDataShow() {
    if (isClicked) {
        allContent.style.display = 'none'; // Hide content
        isClicked = false;
    } else {
        allContent.style.display = 'block'; // Show content
        isClicked = true;
    }
}

